package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 선언
        int a;
        a = 1;
        System.out.println(a);

        int b = 2; // 변수 선언과 초기화를 한꺼번에 할수 있다.
        System.out.println(b);
        int c = 3 , d = 4; // 변수 여러개를 한번에 초기화도 가능하다.
        System.out.println(c);
        System.out.println(d);
    }
}
